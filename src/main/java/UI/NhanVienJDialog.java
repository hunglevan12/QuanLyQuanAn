    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tbNhanVien.getModel();
        model.setRowCount(0);
        try{
//            List<NhanVien>list = dao.selectAll();
             String keyword = txtTim.getText();
             List<NhanVien> list = dao.selectByKeyword(keyword);
            for(NhanVien nv:list){
                Object[]row = {nv.getMaNV(),nv.getPassword(),
                        nv.isVaiTro()?"Admin":"Nhân Viên",
                        nv.getHoTen(),
                    XDate.toString1(nv.getNgaySinh()),
                    nv.getGioiTinh()?"Nam":"Nữ",
                    nv.getSDT()
                };
                model.addRow(row);
            }
        }
        catch(Exception e){
            MsgBox.alert(this, e.getMessage());
        }
    }
    void insert(){
        NhanVien model = getForm();
            try{
                dao.insert(model);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công");
            }catch (Exception e){
                MsgBox.alert(this, e.getMessage());
            }
        
    }
    
    void update(){
        NhanVien model = getForm();
        try{
            dao.update(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Cập nhật thành công");
        }
        catch(Exception e){
            MsgBox.alert(this, e.getMessage());
        }
    }
    
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Chỉ admin mới được xóa nhân viên!");
        }
        else{
            String manv = txtMaNV.getText();
            if(manv.equals(Auth.user.getMaNV())){
                MsgBox.alert(this, "Bạn không được xóa chính mình!");
            }
            else if(MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này?")){
                            try {
                    dao.delete(manv);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                }
                catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }
    void edit(){
        try{
            String maNV = (String)tbNhanVien.getValueAt(this.row, 0);
            NhanVien model = dao.selectById(maNV);
            if(model !=null){
                setForm(model);
                Tabs.setSelectedIndex(0);
            }   
               
            }catch(Exception e){
               MsgBox.alert(this, e.getMessage());
        }
    }
     
