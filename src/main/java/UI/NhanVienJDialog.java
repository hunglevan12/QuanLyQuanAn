    public boolean check16tuoi(JTextField txt) {
        txt.setBackground(white);
        Date date = XDate.toDate(txt.getText());
        Calendar c1 = Calendar.getInstance();
        Calendar c2 = Calendar.getInstance();
        c1.setTime(date);
        c2.setTime(new Date());
        long a = (c2.getTime().getTime() - c1.getTime().getTime()) / (24 * 3600 * 1000);
        if (a >= 5844) {
            return true;
        } else {
            txt.setBackground(pink);
            MsgBox.alert(this, txtNgaySinh.getName() + " phải trên 16 tuổi.");
            return false;
        }
    }
    public boolean checkTrungMa(JTextField txt) {
        txt.setBackground(white);
        if (dao.selectById(txt.getText()) == null) {
            return true;
        } else {
            txt.setBackground(pink);
            MsgBox.alert(this, txtMaNV.getName() + " đã bị tồn tại.");
            return false;
        }
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (check.checkNullText(txtMaNV)
                && check.checkNullPass(txtPass)
                && check.checkNullText(txtHoTen)
                && check.checkNullText(txtNgaySinh)
                && check.checkNullText(txtSDT)){
            if (check.checkMaNV(txtMaNV)
                    && check.checkPass(txtPass)
                    && check.checkName(txtHoTen)
                    && check.checkDate(txtNgaySinh)
                    && check.checkSDT(txtSDT)) {
                if (checkTrungMa(txtMaNV)) {
                     if (check16tuoi(txtNgaySinh)) {
                    insert();
                     }
                }
            }
        }
        
    }//GEN-LAST:event_btnThemActionPerformed

