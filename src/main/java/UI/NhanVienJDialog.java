    void setForm(NhanVien model){
        txtMaNV.setText(model.getMaNV());
        txtPass.setText(model.getPassword());
        rdoAdmin.setSelected(model.isVaiTro());
        rdoNV.setSelected(!model.isVaiTro());
        txtHoTen.setText(model.getHoTen());
        txtNgaySinh.setText(XDate.toString1(model.getNgaySinh()));
        cboGioiTinh.setSelectedIndex(model.getGioiTinh()?0:1);
        txtSDT.setText(model.getSDT());
    }
    NhanVien getForm(){
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setPassword(new String(txtPass.getPassword()));
        nv.setVaiTro(rdoAdmin.isSelected());
        nv.setHoTen(txtHoTen.getText());
        nv.setNgaySinh(XDate.toDate(txtNgaySinh.getText()));
        nv.setGioiTinh(cboGioiTinh.getSelectedIndex() == 0);
        nv.setSDT(txtSDT.getText());
        return nv;
    }

